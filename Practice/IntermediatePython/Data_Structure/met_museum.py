my_artifact = {"artist": "The Michael C. Rockefeller Wing",
               "period": "Medieval",
               "date": "Early 19th century"}

# Print dictionary
print(my_artifact, "\n")

# Print the key
my_artifact_key = my_artifact.keys()
print(f"This is key of my_artifact {my_artifact_key}", "\n")

# Print the value
my_artifact_value = my_artifact.values()
print(f"This is values of my_artifact {my_artifact_value}", "\n")